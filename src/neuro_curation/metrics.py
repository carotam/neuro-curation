def compute_metrics(dataset_dir: Path) -> dict:
    """Compute all four pipeline KPIs for a BIDS dataset.

    This is the main public function. It runs all four KPI checks and
    packages them into a single structured report suitable for:
    - Printing as a text dashboard (format_metrics_report)
    - Saving as JSON for dashboard integration or DMP evidence

    Args:
        dataset_dir: Root of the BIDS dataset (or curation output directory).

    Returns:
        Dict with top-level summary and per-KPI detail sections.
        Keys: dataset, generated_at, summary, kpis
    """
    dataset_dir = Path(dataset_dir)

    bids_rate = _bids_validation_rate(dataset_dir)
    checksum_rate = _checksum_match_rate(dataset_dir)
    fair = _fair_score(dataset_dir)
    completeness = _metadata_completeness(dataset_dir)

    # Summary: how many KPIs met their targets?
    kpi_results = [bids_rate, checksum_rate, fair, completeness]
    targets_met = sum(1 for k in kpi_results if k.get("target_met") is True)
    targets_na = sum(1 for k in kpi_results if k.get("rate") is None)

    return {
        "dataset": str(dataset_dir),
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "summary": {
            "kpis_total": 4,
            "kpis_target_met": targets_met,
            "kpis_unavailable": targets_na,
        },
        "kpis": {
            "bids_validation_rate": bids_rate,
            "checksum_match_rate": checksum_rate,
            "fair_compliance_score": fair,
            "metadata_completeness": completeness,
        },
    }


def format_metrics_report(metrics: dict) -> str:
    """Format KPI metrics as a human-readable dashboard report.

    Args:
        metrics: Output from compute_metrics().

    Returns:
        Multi-line formatted string with per-KPI rows and a summary.
    """
    KPI_LABELS = {
        "bids_validation_rate":  "BIDS Validation Rate     ",
        "checksum_match_rate":   "Checksum Match Rate      ",
        "fair_compliance_score": "FAIR Compliance Score    ",
        "metadata_completeness": "Metadata Completeness    ",
    }

    lines = [
        "=== neuro-curation Pipeline Metrics ===",
        f"Dataset : {metrics['dataset']}",
        f"Run at  : {metrics['generated_at']}",
        "",
        f"{'KPI':<30}  {'Rate':>7}  {'Target':>8}  Status",
        "-" * 62,
    ]

    for key, label in KPI_LABELS.items():
        kpi = metrics["kpis"][key]
        rate = kpi.get("rate")
        target = kpi.get("target")
        met = kpi.get("target_met")

        rate_str   = f"{rate:.1f}%" if rate is not None else "   N/A"
        target_str = f"{target:.0f}%" if target is not None else "    —"
        status_str = "PASS" if met is True else ("FAIL" if met is False else "N/A")

        lines.append(f"{label}  {rate_str:>7}  {target_str:>8}  {status_str}")

    summary = metrics["summary"]
    lines += [
        "-" * 62,
        f"KPIs meeting target: {summary['kpis_target_met']} / "
        f"{summary['kpis_total'] - summary['kpis_unavailable']} available",
        "",
    ]

    # Per-KPI detail for failing metrics
    for key, label in KPI_LABELS.items():
        kpi = metrics["kpis"][key]
        if kpi.get("target_met") is False:
            lines.append(f"[Detail] {label.strip()}")
            issues = kpi.get("issues", {})
            missing_s = issues.get("missing_sidecar", [])
            incomplete = issues.get("incomplete", [])
            mismatches = kpi.get("mismatch_paths", [])
            missing_f  = kpi.get("missing_paths", [])
            if missing_s:
                lines.append(f"  Missing sidecars ({len(missing_s)}):")
                for p in missing_s[:5]:
                    lines.append(f"    {p}")
                if len(missing_s) > 5:
                    lines.append(f"    … and {len(missing_s) - 5} more")
            if incomplete:
                lines.append(f"  Incomplete sidecars ({len(incomplete)}):")
                for item in incomplete[:5]:
                    lines.append(f"    {item['file']} — {item['issue']}")
                if len(incomplete) > 5:
                    lines.append(f"    … and {len(incomplete) - 5} more")
            if mismatches:
                lines.append(f"  Hash mismatches ({len(mismatches)}):")
                for p in mismatches[:5]:
                    lines.append(f"    {p}")
            if missing_f:
                lines.append(f"  Missing files ({len(missing_f)}):")
                for p in missing_f[:5]:
                    lines.append(f"    {p}")
            lines.append("")

    return "\n".join(lines)
